void CharacterManager::make_enemies() //난이도 +2만큼의 적 생성
{
	enemy_count = level + 2;
	enemies = new Enemy[enemy_count];
}
void  CharacterManager::normal_attack()
{
	int my_damage = me->get_damage(); //내 damage
	int enemy_armor = enemies[this_round - 1].get_armor();//적 armor
	int lose_hp = my_damage - enemy_armor;//내 damage - 적 armor
	enemies[this_round - 1].set_my_hp(lose_hp);//잃은 체력 업데이트
	cout << "당신은 상대에게 " << lose_hp << "의 피해를 입혔습니다." << endl;
}
void  CharacterManager::enemy_attack()
{
	int enemy_damage = enemies[this_round - 1].get_damage();//적 damage
	int my_armor = me->get_armor();// 내 방어력
	int lose_hp = enemy_damage - my_armor; // 적 damage - 내 armor
	me->set_my_hp(lose_hp); // 내 잃은 체력 업데이트
	cout << "당신은 상대로부터 " << lose_hp << "의 피해를 입었습니다." << endl;
}
void  CharacterManager::spell_skill()
{
	double my_skill_damage = me->get_skill();//내 damage
	int enemy_armor = enemies[this_round - 1].get_armor();//적 armor
	int lose_hp = (int)my_skill_damage - enemy_armor;// 내 damage - 적 armor
	enemies[this_round - 1].set_my_hp(lose_hp);//잃은 체력 업데이트
	me->set_my_mp(10);//잃은 마나 10 업데이트
	cout << "당신은 상대에게 " << lose_hp << "의 피해를 입혔습니다." << endl;
}

void CharacterManager::play_game()
{
	for (int i = 0; i < enemy_count; i++)
	{
		this_round++; //새로운 적 마다 라운드 추가
		this_round_round = 1; // 새로운 라운드 초기화
		while (1)
		{
			if (me->get_my_hp() <= 0) //체력 방전
			{
				cout << "당신은 패배하였습니다ㅜ" << endl;
				return;
			}
			if (enemies[i].get_my_hp() <= 0) // 적 체력 방전
			{
				cout << "적을 처치 했습니다." << endl;
				cout << endl;
				break;
			}

			cout << "<ROUND " << this_round << " - " << this_round_round << ">" << endl;
			cout << "당신 : HP(" << me->get_my_hp() << ")" << "   " << "MP(" << me->get_my_mp() << ")" << endl;
			cout << "상대 : HP(" << enemies[i].get_my_hp() << ")" << endl;
			cout << "1. 일반공격  2. 스킬사용" << endl;

			while (1)
			{
				cout << "선택: ";
				cin >> num;
				cin.clear();
				cin.ignore(256, '\n');
				cout << endl;
				if ((num == 1) || (num == 2))break;
				else
				{
					cout << "잘못된 공격 방법입니다. 다시 입력해주세요!" << endl;
				}
			}
			if (num == 1)
			{
				normal_attack();
				enemy_attack();
				this_round_round++;
			}
			if (num == 2)
			{
				if (me->get_my_mp() < 10) //마나 부족 스킬 사용 실패
				{
					cout << "MP가 부족합니다. 스킬 사용에 실패했습니다." << endl;
					cout << endl;
					cout << "당신은 상대에게 0의 피해를 입혔습니다." << endl;
					enemy_attack();
					this_round_round++;
				}
				else // 마나 충분 스킬사용 가능
				{
					spell_skill();
					enemy_attack();
					this_round_round++;
				}
			}
		}
	}
	cout << "당신은 승리하셨습니다!" << endl;
	return;
}